#!/bin/bash
__dir=$( cd -- "$( dirname -- "${BASH_SOURCE[0]}" )" &> /dev/null && pwd )
source "$__dir"/../useful_commands.sh
. "${__dir}"/../vars/variables.sh

# Use absolute paths for logs
LOGS_DIR=${__dir}/../../testutil/debugging/logs
mkdir -p "$LOGS_DIR"
LOGS_DIR=$(cd "$LOGS_DIR" && pwd)
rm "$LOGS_DIR"/*.log 2>/dev/null || true

# Save project root for later use
PROJECT_ROOT=$(cd "${__dir}/../.." && pwd)

echo "============================================"
echo "Smart Router Direct RPC (Lava via PublicNode)"
echo "============================================"
echo "Mode: DIRECT RPC (no Lava providers)"
echo "Upstream: PublicNode REST + gRPC + Tendermint RPC"
echo "============================================"
echo ""

# Kill all lavap and lavad processes (best-effort)
killall lavap lavad 2>/dev/null || true
sleep 1

# Kill all screen sessions (best-effort)
killall screen 2>/dev/null || true
sleep 1
screen -wipe
sleep 1

echo "[Test Setup] installing all binaries"
make install-all

echo ""
echo "[Test Setup] skipping local node — using PublicNode endpoints"

# Start cache service (optional but recommended)
echo ""
echo "[Test Setup] starting smart router cache service"
screen -d -m -S cache bash -c "source ~/.bashrc; lavap cache \
127.0.0.1:20100 --metrics_address 0.0.0.0:20200 --log_level debug 2>&1 | tee \"$LOGS_DIR/CACHE.log\"" && sleep 0.25
sleep 2

# PublicNode upstream endpoints (TLS)
LAVA_REST_LOCAL="https://lava-rest.publicnode.com:443"
LAVA_GRPC_LOCAL="grpcs://lava-grpc.publicnode.com:443"
LAVA_TENDERMINTRPC_LOCAL="https://lava-rpc.publicnode.com:443"
LAVA_TENDERMINTRPC_WS_LOCAL="wss://lava-rpc.publicnode.com:443/websocket"

# Static specs (offline) - used for routing/parsing in smart router.
# IMPORTANT: Lava spec imports COSMOSSDK (and related deps), so we must provide the full bundle.
# Note: chain-id verifications in public specs won't match the local dev chain-id ("lava"),
# so we explicitly skip those verifications in node-urls below.
SPECS_DIR="$PROJECT_ROOT/specs/mainnet-1/specs/tendermint.json,$PROJECT_ROOT/specs/mainnet-1/specs/ibc.json,$PROJECT_ROOT/specs/mainnet-1/specs/cosmossdk.json,$PROJECT_ROOT/specs/testnet-2/specs/lava.json"

IFS=',' read -r -a SPEC_FILES <<< "$SPECS_DIR"
for spec_file in "${SPEC_FILES[@]}"; do
	if [ ! -f "$spec_file" ]; then
		echo "✗ ERROR: Spec file not found: $spec_file"
		exit 1
	fi
done

# Generate smart router config (3 upstream REST + 3 gRPC + 3 Tendermint RPC endpoints -> local node)
CONFIG_FILE="$PROJECT_ROOT/smartrouter_lava.yml"
echo ""
if [ -f "$CONFIG_FILE" ]; then
	echo "Preserving existing smart router config: $CONFIG_FILE (not overwriting)"
else
	echo "Generating smart router config: $CONFIG_FILE"
	echo "Upstream REST (LCD):     $LAVA_REST_LOCAL"
	echo "Upstream gRPC:           $LAVA_GRPC_LOCAL"
	echo "Upstream Tendermint RPC: $LAVA_TENDERMINTRPC_LOCAL"
	echo "Upstream Tendermint WS:  $LAVA_TENDERMINTRPC_WS_LOCAL"
	echo ""

cat > "$CONFIG_FILE" <<EOF
# Smart Router Direct RPC Configuration (Lava local node)
# Mode: Direct connections to local lavad endpoints (no Lava providers!)
#
# This file is generated by: scripts/pre_setups/init_lava_smartrouter_lava.sh

endpoints:
  - network-address: "0.0.0.0:3360"
    chain-id: "LAV1"
    api-interface: "rest"

  - network-address: "0.0.0.0:3361"
    chain-id: "LAV1"
    api-interface: "grpc"

  - network-address: "0.0.0.0:3362"
    chain-id: "LAV1"
    api-interface: "tendermintrpc"

direct-rpc:
  # 3 upstream REST endpoints (PublicNode TLS)
  - name: "lava-publicnode-rest-1"
    chain-id: "LAV1"
    api-interface: "rest"
    node-urls:
      - url: "$LAVA_REST_LOCAL"
        timeout: 10s
        skip-verifications:
          - chain-id
          - pruning

  - name: "lava-publicnode-rest-2"
    chain-id: "LAV1"
    api-interface: "rest"
    node-urls:
      - url: "$LAVA_REST_LOCAL"
        timeout: 10s
        skip-verifications:
          - chain-id
          - pruning

  - name: "lava-publicnode-rest-3"
    chain-id: "LAV1"
    api-interface: "rest"
    node-urls:
      - url: "$LAVA_REST_LOCAL"
        timeout: 10s
        skip-verifications:
          - chain-id
          - pruning

  # 3 upstream gRPC endpoints (PublicNode TLS)
  - name: "lava-publicnode-grpc-1"
    chain-id: "LAV1"
    api-interface: "grpc"
    node-urls:
      - url: "$LAVA_GRPC_LOCAL"
        skip-verifications:
          - chain-id
          - pruning

  - name: "lava-publicnode-grpc-2"
    chain-id: "LAV1"
    api-interface: "grpc"
    node-urls:
      - url: "$LAVA_GRPC_LOCAL"
        skip-verifications:
          - chain-id
          - pruning

  - name: "lava-publicnode-grpc-3"
    chain-id: "LAV1"
    api-interface: "grpc"
    node-urls:
      - url: "$LAVA_GRPC_LOCAL"
        skip-verifications:
          - chain-id
          - pruning

  # 3 upstream Tendermint RPC endpoints (PublicNode TLS)
  - name: "lava-publicnode-tendermintrpc-1"
    chain-id: "LAV1"
    api-interface: "tendermintrpc"
    node-urls:
      - url: "$LAVA_TENDERMINTRPC_LOCAL"
        timeout: 10s
        skip-verifications:
          - chain-id
          - pruning
      - url: "$LAVA_TENDERMINTRPC_WS_LOCAL"
        skip-verifications:
          - chain-id
          - pruning

  - name: "lava-publicnode-tendermintrpc-2"
    chain-id: "LAV1"
    api-interface: "tendermintrpc"
    node-urls:
      - url: "$LAVA_TENDERMINTRPC_LOCAL"
        timeout: 10s
        skip-verifications:
          - chain-id
          - pruning
      - url: "$LAVA_TENDERMINTRPC_WS_LOCAL"
        skip-verifications:
          - chain-id
          - pruning

  - name: "lava-publicnode-tendermintrpc-3"
    chain-id: "LAV1"
    api-interface: "tendermintrpc"
    node-urls:
      - url: "$LAVA_TENDERMINTRPC_LOCAL"
        timeout: 10s
        skip-verifications:
          - chain-id
          - pruning
      - url: "$LAVA_TENDERMINTRPC_WS_LOCAL"
        skip-verifications:
          - chain-id
          - pruning
EOF
fi

# Verify config file was created
echo "Verifying generated config file..."
if [ -f "$CONFIG_FILE" ]; then
	FILE_SIZE=$(wc -c < "$CONFIG_FILE")
	echo "✓ Smart router config exists: $CONFIG_FILE (size: $FILE_SIZE bytes)"
else
	echo "✗ ERROR: Smart router config NOT found: $CONFIG_FILE"
	exit 1
fi

echo ""
echo "[Test Setup] starting Smart Router (DIRECT RPC mode, REST + gRPC + Tendermint RPC)"
screen -d -m -S smartrouter bash -c "cd \"$PROJECT_ROOT\" && source ~/.bashrc; lavap rpcsmartrouter \
smartrouter_lava.yml \
--geolocation 1 \
--log-level trace \
--cache-be \"127.0.0.1:20100\" \
--use-static-spec \"$SPECS_DIR\" \
--metrics-listen-address ':7779' 2>&1 | tee \"$LOGS_DIR/SMARTROUTER_LAVA.log\"" && sleep 0.25

sleep 3

echo "Verifying smart router screen session..."
if screen -list | grep -q "smartrouter"; then
	echo "✓ Smart router screen is running"
else
	echo "✗ ERROR: Smart router screen failed to start!"
	echo "  Check $LOGS_DIR/SMARTROUTER_LAVA.log for errors"
	exit 1
fi

echo ""
echo "--- setting up screens done ---"
screen -ls

echo ""
echo "============================================"
echo "Smart Router (Lava via PublicNode) Setup Complete!"
echo "============================================"
echo "Upstream REST:          $LAVA_REST_LOCAL"
echo "Upstream gRPC:          $LAVA_GRPC_LOCAL"
echo "Upstream Tendermint:    $LAVA_TENDERMINTRPC_LOCAL"
echo "Upstream Tendermint WS: $LAVA_TENDERMINTRPC_WS_LOCAL"
echo "Cache:                    127.0.0.1:20100 (metrics: 20200)"
echo "Smart Router REST:        http://127.0.0.1:3360 (metrics: 7779)"
echo "Smart Router gRPC:        127.0.0.1:3361"
echo "Smart Router Tendermint:  http://127.0.0.1:3362"
echo ""
echo "🔬 Test Commands (REST):"
echo "  # Health check (router process)"
echo "  curl -i http://127.0.0.1:3360/lava/health"
echo ""
echo "  # Query latest block via REST (proxied to local lavad LCD)"
echo "  curl http://127.0.0.1:3360/cosmos/base/tendermint/v1beta1/blocks/latest | head"
echo ""
echo "🔬 Test Commands (gRPC):"
echo "  # List available gRPC services"
echo "  grpcurl -plaintext 127.0.0.1:3361 list"
echo ""
echo "  # Query node info via gRPC"
echo "  grpcurl -plaintext 127.0.0.1:3361 cosmos.base.tendermint.v1beta1.Service/GetNodeInfo"
echo ""
echo "  # Query latest block via gRPC"
echo "  grpcurl -plaintext 127.0.0.1:3361 cosmos.base.tendermint.v1beta1.Service/GetLatestBlock"
echo ""
echo "  # Query bank params via gRPC"
echo "  grpcurl -plaintext 127.0.0.1:3361 cosmos.bank.v1beta1.Query/Params"
echo ""
echo "🔬 Test Commands (Tendermint RPC):"
echo "  # Query node status"
echo "  curl http://127.0.0.1:3362/status | jq '.result.sync_info.latest_block_height'"
echo ""
echo "  # Query latest block"
echo "  curl http://127.0.0.1:3362/block | jq '.result.block.header.height'"
echo ""
echo "  # Query validators"
echo "  curl http://127.0.0.1:3362/validators | jq '.result.validators | length'"
echo ""
echo "  # WebSocket subscription (requires wscat: npm install -g wscat)"
echo "  wscat -c ws://127.0.0.1:3362/websocket"
echo "  # Then send: {\"jsonrpc\":\"2.0\",\"id\":1,\"method\":\"subscribe\",\"params\":{\"query\":\"tm.event='NewBlock'\"}}"
echo ""
echo "📊 Monitor Logs:"
echo "  tail -f $LOGS_DIR/SMARTROUTER_LAVA.log"
echo ""
echo "✋ To Stop All Services:"
echo "  killall lavap lavad"
echo "  screen -wipe"
echo "============================================"

